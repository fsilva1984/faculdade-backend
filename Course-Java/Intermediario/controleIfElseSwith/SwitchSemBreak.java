package controleIfElseSwith;

public class SwitchSemBreak {

	public static void main(String[] args) {
		
		int fazes = 6;
		
		switch(fazes) {
		case 6:
			System.out.println("Nasceu Programador!!");
		case 5:
			System.out.println("Entrou na faculdade");
		case 4:
			System.out.println("Passou de Ano");
		case 3:
			System.out.println("Aprendeu a ler");
		case 2:
			System.out.println("Hora da escola");
		case 1:
			System.out.println("Aprende Falar");
		case 0:
			System.out.println("Nasceu!!");
			
		}
	}
}

